/// dart_sexp compiler CLI
///
/// Supports two source formats:
///   .sexp   — S-expression syntax  (Lisp-style)
///   .dmacro — Dart-like syntax     (looks like Dart)
///
/// Both compile to the same Dart output via the same macro engine.
///
/// Usage:
///   dart run bin/sexp.dart compile <file>         — print to stdout
///   dart run bin/sexp.dart compile <file> -o out  — write to file
///   dart run bin/sexp.dart repl                   — interactive REPL
import 'dart:io';

import '../lib/src/builtins.dart';
import '../lib/src/reader.dart';
import '../lib/src/dart_parser.dart';

void main(List<String> args) async {
  registerBuiltins();

  if (args.isEmpty || args.first == 'help') { _usage(); return; }

  switch (args.first) {
    case 'compile': await _compile(args.sublist(1));
    case 'repl':    await _repl();
    default:
      stderr.writeln('Unknown command: ${args.first}');
      _usage(); exit(1);
  }
}

Future<void> _compile(List<String> args) async {
  if (args.isEmpty) {
    stderr.writeln('Usage: sexp compile <file> [-o output.dart]');
    exit(1);
  }

  final inputPath  = args[0];
  final outputPath = args.length >= 3 && args[1] == '-o' ? args[2] : null;
  final source     = await File(inputPath).readAsString();

  try {
    final dart = inputPath.endsWith('.dmacro')
        ? compileDartLike(source)   // Dart-like syntax
        : compile(source);          // S-expression syntax

    final header = '// Generated by dart_sexp from $inputPath\n'
                   '// Do not edit — edit the source file instead.\n\n';
    final output = header + dart;

    if (outputPath != null) {
      await File(outputPath).writeAsString(output);
      stderr.writeln('✓ $inputPath → $outputPath');
    } else {
      print(output);
    }
  } catch (e) {
    stderr.writeln('Error: $e'); exit(1);
  }
}

Future<void> _repl() async {
  print('dart_sexp REPL — type Dart-like code or S-expressions. Ctrl+C to exit.\n');

  while (true) {
    stdout.write('dmacro> ');
    final line = stdin.readLineSync();
    if (line == null) break;
    if (line.trim().isEmpty) continue;

    try {
      // Auto-detect: S-expression starts with (, Dart-like doesn't
      final dart = line.trimLeft().startsWith('(')
          ? compile(line)
          : compileDartLike(line);
      print('→ $dart\n');
    } catch (e) {
      print('Error: $e\n');
    }
  }
}

void _usage() => print('''
dart_sexp — Lisp-style macros that compile to Dart

Usage:
  dart run bin/sexp.dart compile <file.dmacro>          Dart-like syntax → Dart
  dart run bin/sexp.dart compile <file.sexp>            S-expression → Dart
  dart run bin/sexp.dart compile <file> -o output.dart  Write to file
  dart run bin/sexp.dart repl                           Interactive REPL

Dart-like syntax (.dmacro):
  defrecord Payment { double amount; String currency; }
  bool validate(double amount) {
    unless (amount > 0) { throw Exception("invalid"); }
    assertThat(amount <= 1000000);
    return true;
  }

S-expression syntax (.sexp):
  (defrecord Payment (double amount) (String currency))
  (defn bool validate ((double amount))
    (unless (> amount 0) (throw (Exception "invalid")))
    (return true))

Built-in macros (both syntaxes):
  unless / when       Custom control flow
  swap!               Variable swap — injects temp into caller scope
  assertThat          Error message contains the source expression
  withRetry           Retry loop with injected state
  defrecord           Generates full immutable data class
  defunion            Generates sealed class hierarchy
''');
